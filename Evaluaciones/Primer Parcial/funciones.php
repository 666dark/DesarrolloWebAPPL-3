<?php
class Operaciones{

    static function suma($a, $b){
        return $a + $b;
    }
    static function resta($a, $b){
        return $a - $b;
    }
    static function multiplicacion($a, $b){
        return $a * $b;
    }
    static function division($a, $b){
        return $a / $b;
    }

}
?>